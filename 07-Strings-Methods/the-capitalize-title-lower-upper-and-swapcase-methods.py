story = "once upon a time in a land far, far away..."

# Capitalize the first letter of the string
print(story.capitalize()) # Once upon a time in a land far, far away...

# Title case the string 
print(story.title()) # Once Upon A Time In A Land Far, Far Away...

# Convert the string to lowercase
print(story.lower()) # once upon a time in a land far, far away... 

# Convert the string to uppercase
print(story.upper()) # ONCE UPON A TIME IN A LAND FAR, FAR AWAY...

# Swap the case of the string
print(story.swapcase()) # ONCE UPON A TIME IN A LAND FAR, FAR AWAY...  

print(story) #string is unchanged becaue strings are immutable in python
