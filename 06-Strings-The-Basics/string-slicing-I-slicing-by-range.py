address = "Street 1, City 12, Country 123"
print(address[0:6]) # Street Don't forget that the last index is not included
print(address[10:14]) # City 
print(address[19:-3]) # Country
print(address[0:100]) # Street 1, City 12, Country 123

print(address[5:]) # starts at index 5 and goes to the end
print(address[:5]) # starts at the beginning and goes to index 5
print(address[:]) # starts at the beginning and goes to the end

