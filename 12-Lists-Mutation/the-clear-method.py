citrus_fruits = ['orange', 'lemon', 'grapefruit', 'pineapple', 'mandarin orange']
citrus_fruits.clear()
print(citrus_fruits)

# The clear method removes all items from a list.
# The clear method doesn't take any parameters.
# The clear method returns None.
# The clear method is useful when you want to empty a list without having to create a new list.