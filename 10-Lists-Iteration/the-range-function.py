#range returns a list of numbers from 0 to the number you pass in
# it is the type of list that is called a generator
# it is a list that is not stored in memory

for number in range(5): # 0,1,2,3,4
    print(number)

for number in range(3, 5): # 3,4
    print(number)

for number in range(0, 10, 2): # 0,2,4,6,8
    print(number)

 

